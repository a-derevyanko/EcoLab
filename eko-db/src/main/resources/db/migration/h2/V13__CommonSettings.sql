CREATE TABLE COMMON_SETTINGS (
  id      BIGINT GENERATED BY DEFAULT AS IDENTITY ( START WITH 0) PRIMARY KEY,
  version INT,
  name    VARCHAR(256) NOT NULL,
  value   VARCHAR(256) NOT NULL,
  type    VARCHAR(256) NOT NULL,
);

/*
* Добавим комментарии
*/
COMMENT ON TABLE COMMON_SETTINGS IS 'Параметры системы';
COMMENT ON COLUMN COMMON_SETTINGS.name IS 'Название параметра';
COMMENT ON COLUMN COMMON_SETTINGS.value IS 'Значение параметра';
COMMENT ON COLUMN COMMON_SETTINGS.type IS 'Тип параметра';

